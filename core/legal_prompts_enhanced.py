def format_enhanced_legal_prompt(query: str, context_chunks: list) -> str:
    """
    Форматирует промпт с few-shot примерами и контекстом

    Args:
        query: Вопрос пользователя
        context_chunks: Найденные документы

    Returns:
        Полный промпт для модели
    """

    # Форматируем контекст
    context_text = ""
    for i, chunk in enumerate(context_chunks, 1):
        if isinstance(chunk, dict):
            text = chunk.get("text", "")
            law = chunk.get("metadata", {}).get("law", chunk.get("law_number", "unknown"))
            similarity = chunk.get("similarity", chunk.get("rrf_score", 0))

            context_text += f"\n{'='*60}\n"
            context_text += f"ДОКУМЕНТ {i} из {len(context_chunks)}\n"
            context_text += f"[Закон: {law}] [Релевантность: {similarity:.3f}]\n"
            context_text += f"{'='*60}\n\n"
            context_text += f"{text}\n"
        else:
            context_text += f"\n{'='*60}\n"
            context_text += f"ДОКУМЕНТ {i}\n"
            context_text += f"{str(chunk)}\n"

    from core.prompt_sanitizer import sanitize_query, sanitize_context
    safe_query = sanitize_query(query)
    safe_context = sanitize_context(context_text)

    user_prompt = f"""
ВАША ЗАДАЧА: Ответьте на правовой вопрос, используя ТОЛЬКО предоставленные документы.

ВОПРОС: {safe_query}

ПРЕДОСТАВЛЕНО ДОКУМЕНТОВ: {len(context_chunks)}

ДОКУМЕНТЫ:
{safe_context}

---

ВАЖНЫЕ ИНСТРУКЦИИ:
1. Отвечайте СТРОГО на основе предоставленных документов
2. ОБЯЗАТЕЛЬНО указывайте статьи и номера законов
3. Если информации недостаточно - честно скажите об этом
4. Следуйте формату из примеров выше
5. Не домысливайте - только факты из документов

ВАШЕ ОТВЕТ:
"""

    return user_prompt


def get_enhanced_legal_system_prompt() -> str:
    """Возвращает улучшенный системный промпт"""
    return ENHANCED_SYSTEM_PROMPT


def format_verification_friendly_answer(answer: str, sources: list) -> dict:
    """
    Форматирует ответ для удобной верификации

    Args:
        answer: Сгенерированный ответ
        sources: Использованные источники

    Returns:
        Структурированный ответ
    """
    return {
        "answer": answer,
        "sources_used": len(sources),
        "laws_mentioned": extract_law_numbers(answer),
        "articles_mentioned": extract_article_numbers(answer)
    }


def extract_law_numbers(text: str) -> list:
    """Извлекает упоминания законов из текста"""
    import re
    pattern = r'(?:Федеральный закон|ФЗ|N\s*)?(\d{1,3})\s*-\s*ФЗ'
    matches = re.findall(pattern, text, re.IGNORECASE)
    return list(set([f"{m}-ФЗ" for m in matches]))


def extract_article_numbers(text: str) -> list:
    """Извлекает упоминания статей из текста"""
    import re
    patterns = [
        r'[Сс]татья\s+(\d+)',
        r'[Сс]татьи\s+(\d+)',
        r'[Сс]т\.\s*(\d+)'
    ]
    articles = []
    for pattern in patterns:
        matches = re.findall(pattern, text)
        articles.extend([f"Статья {m}" for m in matches])
    return list(set(articles))